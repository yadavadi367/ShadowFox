# In a village, there is a circular pond with a radius of 84 meters.Calculate the area of the pond using the formula: Circle Area = πr^2. (Use the value 3.14 for π) Bonus Question: If there is exactly1.4 liters of water in a square meter, what is the total amount of water in the pond? Print the answer without any decimal point init. Hint: Circle Area = π r^2 Water in the pond = Pond AreaWater per Square Meter.

# Given values
pi = 3.14
r = 84
water_per_sq_m = 1.4

# Calculate area of the pond
area = pi * r * r

# Calculate total water in the pond
total_water = area * water_per_sq_m

# Print results
print("Area of the pond =", area)
print("Total water in the pond =", int(total_water))
